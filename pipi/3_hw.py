# 6. Функция на вход получает список, состоящий из 5 произвольных чисел.
# Найти количество положительных чисел среди них.
def quantity(x: list):
    c=0
    for i in x:
        if i>0:
            c=c+1
    print(c)
quantity(x=[5, 6, 7, 6, 4])

